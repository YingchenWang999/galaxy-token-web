export type Language = 'zh' | 'en'

export const LANGUAGE_STORAGE_KEY = 'gxly-language'

export function resolveInitialLanguage(
  savedLanguage?: string | null,
  browserLanguage?: string,
): Language {
  if (savedLanguage === 'zh' || savedLanguage === 'en') return savedLanguage
  return browserLanguage?.toLowerCase().startsWith('zh') ? 'zh' : 'en'
}

export function getInitialLanguage(): Language {
  try {
    return resolveInitialLanguage(
      window.localStorage.getItem(LANGUAGE_STORAGE_KEY),
      window.navigator.languages?.[0] ?? window.navigator.language,
    )
  } catch {
    return resolveInitialLanguage(null, window.navigator.language)
  }
}

export function localizeWalletError(detail: string, language: Language) {
  if (language === 'zh') return detail

  const exactMessages: Record<string, string> = {
    '接收地址格式不正确': 'The recipient address is invalid.',
    '接收地址不能是零地址': 'The recipient address cannot be the zero address.',
    '接收地址不能与当前钱包相同': 'The recipient address cannot be the connected wallet.',
    '请输入正确的 GXLY 数量': 'Enter a valid GXLY amount.',
    '发送数量必须大于 0': 'The amount must be greater than 0.',
    'GXLY 余额不足': 'Insufficient GXLY balance.',
    '钱包未返回详细原因': 'The wallet did not provide more details.',
  }

  if (exactMessages[detail]) return exactMessages[detail]
  const precisionMatch = detail.match(/^GXLY 最多支持 (\d+) 位小数$/)
  return precisionMatch
    ? `GXLY supports up to ${precisionMatch[1]} decimal places.`
    : detail
}

